package org.dbpedia.moss.db;

public class APIKeyInfo {
    
    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    private String key;

    public void SetKey(String key) {
        this.key = key;
    }   

    public String getKey() {
        return key;
    }
}
